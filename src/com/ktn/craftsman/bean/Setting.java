package com.ktn.craftsman.bean;

public class Setting {
	private String appVersion;
	private String downloadAndroid;
	private String consumerRegisterAgreement;
	private String phone;
	private String email;

	public String getAppVersion() {
		return appVersion;
	}

	public void setAppVersion(String appVersion) {
		this.appVersion = appVersion;
	}

	public String getDownloadAndroid() {
		return downloadAndroid;
	}

	public void setDownloadAndroid(String downloadAndroid) {
		this.downloadAndroid = downloadAndroid;
	}

	public String getConsumerRegisterAgreement() {
		return consumerRegisterAgreement;
	}

	public void setConsumerRegisterAgreement(String consumerRegisterAgreement) {
		this.consumerRegisterAgreement = consumerRegisterAgreement;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}
	
}
